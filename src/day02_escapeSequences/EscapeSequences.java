package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello everyone\nToday we have Java class");

        System.out.println("------------------------------------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Developer Course Batch 29");

        System.out.println("------------------------------------------------------");

        System.out.println("\\");

        System.out.println("------------------------------------------------------");

        System.out.println("My favorite TV show is \"The Walking Dead\"");


    }
}

/*
    1. \n: New Line
    2. \t: paragraph Space
    3. \\: print 1 backslash
    4. \": double quote

 */