package day04_concatenation;

public class ArithmeticOperators {

    public static void main(String[] args) {

        System.out.println( 100 + 200 );

        System.out.println( 100 - 50);

        // int / int ===> int
        // int / double ===> double
        // double / int ===> double
        // double / double ===> double
        System.out.println( 50 / 6);

        System.out.println( 50 / 6.0);

        System.out.println( 50.0 / 6);

        System.out.println( 50.0 / 6.0);

        System.out.println( 50 / 6);

        System.out.println((int)(2.5 / 0.5));

        System.out.println(18 * 2);

        System.out.println(15/4.0);

        // System.out.println(9 / 0); can not division to zero

        // Modulus
        System.out.println(10%3);

        System.out.println(20%6);

        System.out.println(15%4);

        System.out.println(100%13);

        System.out.println(45%8);






    }
}
