package WednesdayLiveSession.Week6;

public class NestedLoopExample {

    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 4; j++) {
                System.out.println(i + " " + j);
            }
        }
        System.out.println("Inner Loop Ended");


    }
}
